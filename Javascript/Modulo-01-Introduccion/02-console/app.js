// Consola

// La consola es un panel que muestra mensajes importantes, como errores, para los desarrolladores. Gran parte del trabajo que la computadora hace con nuestro código es invisible para nosotros por defecto. Si queremos ver las cosas que aparecen en nuestra pantalla, podemos imprimir o iniciar sesión en nuestra consola directamente.

// En JavaScript, la palabra clave consola se refiere a un objeto, una colección de datos y acciones, que podemos usar en nuestro código. Las palabras clave son palabras que están integradas en el lenguaje JavaScript, por lo que la computadora las reconoce y las trata de manera especial.

// Una acción o método integrado en el objeto de la consola es el método .log(). Cuando escribimos console.log(), lo que ponemos entre paréntesis se imprimirá o registrará en la consola.

// Nos será muy útil imprimir valores en la consola, para que podamos ver el trabajo que estamos haciendo.

// consola.log(5);

// Este ejemplo registra 5 en la consola. El punto y coma denota el final de la línea o declaración. Aunque en JavaScript su código generalmente se ejecutará según lo previsto sin un punto y coma, le recomendamos que aprenda el hábito de terminar cada declaración con un punto y coma para que nunca omita uno en los pocos casos en que sea necesario.

