let season = 'primavera';

if (season === 'primavera') {
  console.log('Es Primavera! los arboles florecen');
}

else if (season === 'invierno') {
    console.log('¡Es invierno! Todo está cubierto de nieve.');
  }

else if (season === 'otoño') {
    console.log('¡Es otoño! ¡Las hojas están cayendo!');
  }

else if (season === 'verano') {
console.log('¡Hace calor porque es verano!');
}

else {
  console.log('Estación inválida.');
}